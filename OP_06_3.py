import random

def выбрать_учеников(список_учеников, количество=5):
    # Проверяем, чтобы количество выбираемых учеников не превышало длину списка
    if количество > len(список_учеников):
        raise ValueError("Количество выбираемых учеников не может превышать количество доступных учеников.")
    # Случайный выбор уникальных имён
    выбранные_ученики = random.sample(список_учеников, количество)
    return выбранные_ученики

# Пример входного списка имён
ученики = [
    "Алексей", "Мария", "Дмитрий", "Анна", "Сергей",
    "Елена", "Игорь", "Татьяна", "Павел", "Ольга",
    "Николай", "Евгения", "Константин", "Ксения", "Роман"
]
try:
    выбранные = выбрать_учеников(ученики)
    print("Выбранные ученики для ответа на уроке:")
    for ученик in выбранные:
        print(ученик)
except ValueError as e:
    print(e)



