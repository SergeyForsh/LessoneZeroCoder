def calculator():
    print("Простой калькулятор")

    # Ввод двух чисел
    num1 = float(input("Введите первое число: "))
    num2 = float(input("Введите второе число: "))

    # Выбор операции
    print("Выберите операцию:")
    print("1. Сложение (+)")
    print("2. Вычитание (-)")
    print("3. Умножение (*)")
    print("4. Деление (/)")

    choice = input("Введите номер операции (1/2/3/4): ")

    if choice == '1':
        result = num1 + num2
        print(f"{num1} + {num2} = {result}")
    elif choice == '2':
        result = num1 - num2
        print(f"{num1} - {num2} = {result}")
    elif choice == '3':
        result = num1 * num2
        print(f"{num1} * {num2} = {result}")
    elif choice == '4':
        if num2 != 0:
            result = num1 / num2
            print(f"{num1} / {num2} = {result}")
        else:
            print("Ошибка: Деление на ноль!")
    else:
        print("Неверный ввод. Пожалуйста, выберите операцию от 1 до 4.")


# Запуск калькулятора
calculator()


### Как это работает:
# 1. Программа запрашивает у пользователя ввод двух чисел.
# 2. Затем выводится меню с доступными арифметическими операциями.
# 3. Пользователь выбирает операцию, вводя соответствующий номер.
# 4. В зависимости от выбора, программа выполняет соответствующее арифметическое действие и выводит результат.
# 5. Если пользователь выбрал деление и второе число равно нулю, программа выводит сообщение об ошибке.