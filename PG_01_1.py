# Функция для вычисления n + nn + nnn
def calculate_expression(n):
    # Преобразуем n в строку, чтобы создать nn и nnn
    str_n = str(n)
    nn = int(str_n * 2)  # Создаем nn
    nnn = int(str_n * 3) # Создаем nnn
    result = n + nn + nnn
    return result

# Запрашиваем ввод у пользователя
n = int(input("Введите целое число n: "))
result = calculate_expression(n)
print(f"Результат n + nn + nnn для n = {n} равен: {result}")


### Как работает этот код:
# 1. Определяется функция `calculate_expression`, которая принимает целое число `n`.
# 2. Число `n` преобразуется в строку для создания чисел `nn` и `nnn` путём их повторения.
# 3. Затем `nn` и `nnn` преобразуются обратно в целые числа.
# 4. Считается сумма \( n + nn + nnn \).
# 5. Вводится значение `n` от пользователя, и выводится результат.