def is_palindrome(s):
    # Убираем пробелы и переводим строку в нижний регистр
    s = s.replace(" ", "").lower()
    # Сравниваем строку с её обратной версией
    return s == s[::-1]

# Примеры использования

test_strings = ["А роза упала на лапу Азора", "hello", "level", "Python"]

for string in test_strings:
    if is_palindrome(string):
        print(f'"{string}" является палиндромом.')
    else:
        print(f'"{string}" не является палиндромом.')

# Этот код сначала удаляет пробелы и переводит строку в нижний регистр, затем сравнивает строку с её обратной версией.
# Вы можете заменить список `test_strings` на любые строки, которые хотите проверить.

